# Decalracion de una variable 

"""Nunca debe empezar por un numero , pueden empezar por _ , es case sensitive."""

name = "Gabriel"
Name = "Sebastian"

print("Hola "+name)
print("Hola "+Name)

# Variables numericas

num1 = 1
num2 = 5

resultado = num1 + num2
resultado2 = num2 - num1

# encadenar

"""Solo podes concantenar(+) variables de string, para mostrar numeros, se utiliza la , o la otra forma es con f{}"""

#Ejemplo con ,
print("El resultado de la suma es:",resultado)

#Ejemplo con f{}
print(f"El resultado de la resta es: {resultado2}")

